# cube

cube = lambda num: num**3
print(cube(3))


sq = lambda num1: num1**2
print(sq(3))


addition = lambda num1 , num2 : num1 + num2

print(addition(10,20))

max_two = lambda num1,num2: num1 if num1 < num2 else num1
print(max_two(10,40))


min_two = lambda num1,num2: num1 if num1 > num2 else num2
print(min_two(10,50))


sub= lambda num1,num2: num1 - num2
print(sub(10,5))

